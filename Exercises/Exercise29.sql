USE NikamoozDB;
GO

DROP TABLE IF EXISTS A, B;
GO

CREATE TABLE A(ID TINYINT);
GO

CREATE TABLE B(ID TINYINT);
GO

INSERT INTO A VALUES(1), (2), (3), (4);
GO

INSERT INTO B VALUES(2), (NULL);
GO

SELECT A.ID FROM dbo.A
JOIN B
	ON A.ID <> B.ID;
GO

SELECT
	ID
FROM dbo.A
	WHERE NOT EXISTS
		(
			SELECT 1 FROM dbo.B
			WHERE A.ID = B.ID
		);
GO

SELECT
	ID
FROM dbo.A
	WHERE EXISTS
		(
			SELECT 1 FROM dbo.B
			WHERE A.ID <> B.ID
		);
GO

SELECT
	A.ID
FROM dbo.A
	WHERE A.ID NOT IN
		(
			SELECT B.ID FROM dbo.B
			WHERE A.ID = B.ID
		);
GO

SELECT
	A.ID
FROM dbo.A
	WHERE A.ID NOT IN
		(
			SELECT B.ID FROM dbo.B
			WHERE B.ID IS NOT NULL
		);
GO

SELECT
	A.ID
FROM dbo.A
	WHERE A.ID NOT IN
		(
			SELECT ISNULL(B.ID, '') FROM dbo.B
		);
GO
